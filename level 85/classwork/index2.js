// კლასური დავალება - ფუნქციების შედგენა

// 1. function keyword-ით შედგენილი ფუნქცია კონსტანტაში
const regularFunction = function() {
    console.log("hello");
};

// 2. arrow ფუნქცია კონსტანტაში
const arrowFunction = () => {
    console.log("hello");
};

// ფუნქციების გამოძახება
console.log("Regular function:");
regularFunction();

console.log("Arrow function:");
arrowFunction();
